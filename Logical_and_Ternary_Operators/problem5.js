let weekDay = "Fri";

switch(weekDay) {
  case "Mon":
    console.log("Monday");
    break;
  case "Tue":
    console.log("Tuesday");
    break;
  case "Wed":
    console.log("Wednesday");
    break;
  case "Thur":
    console.log("Thurday");
    break;
  case "Fri":
    console.log("Friday");
    break;
  case "Sat":
    console.log("Saturday");
    break;
  case "Sun":
    console.log("Sunday");
    break;
  default:
    console.log("Not a valid weekday");
}